#include <stdio.h>

// Κυρίως πρόγραμμα
int main() {
  // Δήλωση μεταβλητών
  int a = 5, b = 4, sum = 0, diff = 0;

  // Κλήση της συνάρτησης badf
  badf(a, b, sum, diff);

  // Εμφάνιση των αποτελεσμάτων
  printf("Sum: %d\n", sum);
  printf("Diff: %d\n", diff);

  return 0;
}

// Το badf αλλάζει τις τιμές των sum και diff, αλλά επειδή οι μεταβλητές
// περνιούνται ως τιμές και όχι ως δείκτες, οι αλλαγές δεν αποθηκεύονται στο
// κυρίως πρόγραμμα. Τα αποτελέσματα του sum και diff θα είναι 0 καθώς δεν
// αλλάζουν από τη συνάρτηση.

// Μεταγλώττιση:
// gcc -c myfun.c
// gcc -c myprog.c
// gcc -o myprog myfun.o myprog.o

// Εκτέλεση:
// ./myprog

// Αναμενόμενο αποτέλεσμα:
// Sum: 0
// Diff: 0

// Αν η συνάρτηση δεν δηλωθεί σωστά, θα εμφανιστεί το μήνυμα: Undefined
// reference to 'badf'.