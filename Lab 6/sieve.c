#include <stdio.h>
#define N 50  // Μέγιστη τιμή εύρους αριθμών

int main() {
  int A[N + 1];  // Πίνακας για τους αριθμούς από 2 έως N
  int i, j;

  // Αρχικοποίηση του πίνακα: Όλοι οι αριθμοί θεωρούνται πιθανοί πρώτοι
  for (i = 2; i <= N; i++) {
    A[i] = 1;
  }

  // Κόσκινο του Ερατοσθένη
  for (i = 2; i <= N; i++) {
    if (A[i] == 1) {  // Αν ο αριθμός είναι πρώτος
      for (j = 2 * i; j <= N; j += i) {  // Διαγραφή των πολλαπλασίων του
        A[j] = 0;
      }
    }
  }

  // Εμφάνιση των πρώτων αριθμών
  printf("Prime numbers up to %d are:\n", N);
  for (i = 2; i <= N; i++) {
    if (A[i] == 1) {
      printf("%d ", i);
    }
  }
  printf("\n");

  return 0;
}

// Ο πίνακας A αρχικοποιείται με 1 για όλους τους αριθμούς από 2 έως N,
// υποδηλώνοντας ότι όλοι είναι πιθανοί πρώτοι.

// Με τη μέθοδο του κόσκινου:
// Κάθε πρώτος αριθμός χρησιμοποιείται για να διαγράψει όλα τα πολλαπλάσιά του
// (δηλαδή, θέτονται σε 0). Στο τέλος, οι αριθμοί που παραμένουν με τιμή 1 στον
// πίνακα είναι οι πρώτοι.

