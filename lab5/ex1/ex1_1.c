/* 
    Στην C το (5 > 1) σαν παράσταση επιστρέφει έναν αριθμό != 0.
    Από την άλλη, το (1 > 5) επιστρέφει 0.
    Άρα, ένας κομψός τρόπος να λύσουμε αυτήν την άσκηση είναι χρησιμοποιόντας το εξής σκεπτικό:
    Οι περιττοί αριθμοί έχουν υπόλοιπο διάιρεσης με το 2 πάντα != 0, σε αντίθεση με τους άρτιους που είναι πάντα 0.
    Συνεπώς, επιστρέφοντας το υπόλοιπο της διάιρεσης, ξέρουμε πως να την αξιοποιήσουμε.
*/

int isodd(int n)
{
    return n % 2;
}